// 有効にする場面 ビットフラグ
#define CMD_NONE        0     // 表示しない
#define CMD_SHORTCUTKEY 1     // ショートカットキーとして設定可能
#define CMD_HOTKEY      1<<1  // グローバルホットキーとして設定可能
#define CMD_MENU        1<<2  // メニュー項目として使用（11.12.21現在、表示制御は行なっていない）
#define CMD_TOOLBAR     1<<3  // ツールバーで使用（11.12.21現在、表示制御は行なっていない）

#define CMD_KEYSET      (CMD_SHORTCUTKEY|CMD_HOTKEY)
#define CMD_KEYMENU     (CMD_KEYSET|CMD_MENU)
#define CMD_ALL         (CMD_KEYSET|CMD_MENU|CMD_TOOLBAR)


//--------------------------------------------------------------------------
// STRUCT
