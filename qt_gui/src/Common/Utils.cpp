#include "Utils.h"
#include <stdlib.h>
#include <time.h>
//--------------------------------------------------------------------------
int CUtils::GetRandom(int nMin, int nMax)
{
	srand(time(nullptr));
	return nMin + (int)(rand() * (nMax - nMin + 1.0) / (1.0 + RAND_MAX));
}
